export function updateName(name: any) {
    return  {
        type:'updateName',
        payload:{name:name}
    }
}

export function updateEmail(name: any) {
    return  {
        type:'updateEmail',
        payload:{email:name}
    }
}

export function updateMobile(name: any) {
    return  {
        type:'updateMobile',
        payload:{mobile:name}
    }
}